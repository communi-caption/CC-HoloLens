namespace CC_HoloLens
{
    /// <summary>
    /// An empty page that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class MainPage : Page
    {
        public MainPage()
        {
            this.InitializeComponent();
            this.Loaded += MainPage_Loaded;
        }

        private async void MainPage_Loaded(object sender, RoutedEventArgs e)
        {
            await FaceDetectionUtils.Initialize();
            var mediaCapture = await FaceDetectionUtils.WebcamPreview(previewControl);

            while (true)
            {
                await Task.Delay(20);
                var bitmap = await FaceDetectionUtils.GetSoftwareBitmapFromWebcam(mediaCapture);
                var detected = await FaceDetectionUtils.IsFaceDetected(bitmap);

                Debug.WriteLine(detected + "");
            }
        }
    }
}
